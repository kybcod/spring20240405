package com.study.domain;

public class MyBean092 {
    private String name;
    private Integer age;

    // 필드명이 property명일 확률이 높지만 무조건은 아니다
    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Integer getAge() {
        return age;
    }

    public void setAge(Integer age) {
        this.age = age;
    }

}
